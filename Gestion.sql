CREATE TABLE FOURNISSEUR
(NumF CHAR(30)  NOT NULL, NomF VARCHAR(20) NOT NULL, TelF VARCHAR(20) NOT NULL, AdrF VARCHAR(20) NOT NULL, PRIMARY KEY (NumF)); 

CREATE TABLE PRODUIT
(NumF CHAR(30) NOT NULL, NomP VARCHAR(20) NOT NULL, QteP INTEGER NOT NULL, Prix INTEGER NOT NULL, Total INTEGER NOT NULL , PRIMARY KEY (NumF, NomP), FOREIGN KEY (NumF) REFERENCES FOURNISSEUR(NumF) ON DELETE CASCADE); 


 CREATE TABLE CLIENT
 (NumC CHAR(30)  NOT NULL, NomC VARCHAR(20) NOT NULL, PrenomC VARCHAR(20) NOT NULL,  TelC VARCHAR(20) NOT NULL, AdrC VARCHAR(20) NOT NULL, PRIMARY KEY (NumC)); 



CREATE TABLE COMMANDE  
(NumCom CHAR(30)  NOT NULL, NumC CHAR(30) NOT NULL, NomP VARCHAR(20) NOT NULL, QteC INTEGER NOT NULL, PRIMARY KEY (NumCom, NumC, NomP), FOREIGN KEY (NumC) REFERENCES CLIENT(NumC) ON DELETE CASCADE);




